package sk.drakkar.oar.authors;

public class AuthorListBuildingException extends RuntimeException {

	public AuthorListBuildingException() {
		super();
	}

	public AuthorListBuildingException(String message, Throwable cause) {
		super(message, cause);
	}

	public AuthorListBuildingException(String message) {
		super(message);
	}

	public AuthorListBuildingException(Throwable cause) {
		super(cause);
	}
	
}
