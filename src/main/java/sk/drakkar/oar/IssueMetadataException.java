package sk.drakkar.oar;

public class IssueMetadataException extends RuntimeException {

	public IssueMetadataException() {
		super();
	}

	public IssueMetadataException(String message, Throwable cause) {
		super(message, cause);
	}

	public IssueMetadataException(String message) {
		super(message);
	}

	public IssueMetadataException(Throwable cause) {
		super(cause);
	}
	
}
