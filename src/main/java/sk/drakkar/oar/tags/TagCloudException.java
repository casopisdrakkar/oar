package sk.drakkar.oar.tags;

public class TagCloudException extends RuntimeException {

	public TagCloudException() {
		super();
	}

	public TagCloudException(String message, Throwable cause) {
		super(message, cause);
	}

	public TagCloudException(String message) {
		super(message);
	}

	public TagCloudException(Throwable cause) {
		super(cause);
	}
	
}
