package sk.drakkar.oar.homepage;

public class HomePageBuildingException extends RuntimeException {

	public HomePageBuildingException() {
		super();
	}

	public HomePageBuildingException(String message, Throwable cause) {
		super(message, cause);
	}

	public HomePageBuildingException(String message) {
		super(message);
	}

	public HomePageBuildingException(Throwable cause) {
		super(cause);
	}

}
