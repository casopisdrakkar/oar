package sk.drakkar.oar;

public class ProjectHierarchyException extends RuntimeException {

	public ProjectHierarchyException() {
		super();
	}

	public ProjectHierarchyException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProjectHierarchyException(String message) {
		super(message);
	}

	public ProjectHierarchyException(Throwable cause) {
		super(cause);
	}
	
}
