package sk.drakkar.oar;


public class ArticleProcessException extends RuntimeException {

	public ArticleProcessException() {
		super();
	}

	public ArticleProcessException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArticleProcessException(String message) {
		super(message);
	}

	public ArticleProcessException(Throwable cause) {
		super(cause);
	}
	

}
