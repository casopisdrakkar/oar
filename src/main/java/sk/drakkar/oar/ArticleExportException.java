package sk.drakkar.oar;

public class ArticleExportException extends RuntimeException {

	public ArticleExportException() {
		super();
	}

	public ArticleExportException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArticleExportException(String message) {
		super(message);
	}

	public ArticleExportException(Throwable cause) {
		super(cause);
	}
	
}
